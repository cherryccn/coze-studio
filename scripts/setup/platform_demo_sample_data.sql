SELECT '--- billing_records ---' AS section;
SELECT COUNT(*) AS total_records,
       SUM(CASE WHEN status='success' THEN 1 ELSE 0 END) AS success_count,
       SUM(CASE WHEN status='failed' THEN 1 ELSE 0 END) AS failed_count,
       SUM(CASE WHEN status='refund' THEN 1 ELSE 0 END) AS refund_count,
       ROUND(SUM(amount), 2) AS total_amount,
       SUM(usage_tokens) AS total_tokens
FROM billing_records WHERE request_id LIKE 'demo-req-%';

SELECT '--- billing_daily_agg ---' AS section;
SELECT COUNT(*) AS total_rows,
       COUNT(DISTINCT dt) AS days_covered,
       COUNT(DISTINCT space_id) AS spaces_covered
FROM billing_daily_agg;
